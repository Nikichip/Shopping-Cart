num1=int(input("Enter Number:"))
num2=int(input("Enter Number:"))
operation=input("Enter an operator:")
if operation=='+':
    print (num1,'+',num2,'=',num1+num2)
elif operation=='-':
    print (num1,'-',num2,'=',num1-num2)
elif operation=='/':
    print (num1,'/',num2,'=',num1/num2)
elif operation=='*':
    print (num1,'*',num2,'=',num1*num2)
elif operation=='%':
    print (num1,'%',num2,'=',num1%num2)
elif operation=='**':
    print (num1,'**',num2,'=',num1**num2)
elif operation=='//':
    print (num1,'//',num2,'=',num1//num2)
